export function getLocalizedCircularDestinations(destinations: any[], lang: string = "en"): any[] {
  if (lang === "en" || !destinations) return destinations;
  const langDB = DESTINATION_TRANSLATIONS[lang];
  if (!langDB) return destinations;

  return destinations.map((dest) => {
    const translation = langDB[dest.title];
    if (translation) {
      return {
        ...dest,
        title: translation.title,
        description: translation.desc
      };
    }
    return dest;
  });
}

export function getLocalizedTickerText(bannerTickerText: string[], lang: string = "en"): string[] {
  if (lang === "en" || !bannerTickerText) return bannerTickerText;
  return TICKER_TRANSLATIONS[lang] || bannerTickerText;
}
