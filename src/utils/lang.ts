// Generic translator function to replace words in any string
export function translateString(text: string, lang: string): string {
  if (!text || lang === "en") return text;
  const glossary = COMMON_GLOSSARY[lang];
  if (!glossary) return text;

  let translated = text;
  Object.keys(glossary).forEach((englishWord) => {
    const rx = new RegExp(englishWord, "gi");
    translated = translated.replace(rx, glossary[englishWord]);
  });
  return translated;
}

// Localized mapping processors
export function getLocalizedPackages(packages: any[], lang: string = "en"): any[] {
  if (lang === "en" || !packages) return packages;
  
  return packages.map((pkg) => {
    const langDB = DATA_LOCALIZATION[lang];
    const itemTranslations = langDB ? langDB[pkg.id] : null;

    if (itemTranslations) {
      return {
        ...pkg,
        title: itemTranslations.title || pkg.title,
        badge: itemTranslations.badge || pkg.badge,
        duration: itemTranslations.duration || pkg.duration,
        fromRoute: itemTranslations.fromRoute || pkg.fromRoute,
        overview: itemTranslations.overview || pkg.overview,
        difficulty: itemTranslations.difficulty || pkg.difficulty,
        highlights: itemTranslations.highlights || pkg.highlights,
        inclusion: itemTranslations.inclusion || pkg.inclusion,
        exclusion: itemTranslations.exclusion || pkg.exclusion,
        itinerary: pkg.itinerary.map((dayItem: any) => {
          const matchedDay = itemTranslations.itinerary?.find((d: any) => d.day === dayItem.day);
          return matchedDay ? { ...dayItem, title: matchedDay.title, details: matchedDay.details } : dayItem;
        })
      };
    } else {
      // Fallback: translate common parts of custom-created packages
      return {
        ...pkg,
        title: translateString(pkg.title, lang),
        badge: translateString(pkg.badge, lang),
        duration: translateString(pkg.duration, lang),
        fromRoute: translateString(pkg.fromRoute, lang),
        difficulty: translateString(pkg.difficulty, lang) as any
      };
    }
  });
}

